## 强制硬规则

- 禁止凭空捏造客户名/产品名/接口/字段，未知项写 `[待补充]`
- 禁止省略章节，按模板表头逐节填写；用户输入不足的章节用 `（用户暂未提供，请补充）` 占位
- 不得加任何 emoji 字符
";

