- 使用 Markdown 格式，结构清晰、层次分明
- 关键信息使用表格、列表突出显示
- 接口定义、数据模型用代码块
- 一次响应里**先输出 Part A 完整内容**，结尾给出『请确认 Part A 方向是否符合预期 → 确认后我再输出 Part B』的提示
