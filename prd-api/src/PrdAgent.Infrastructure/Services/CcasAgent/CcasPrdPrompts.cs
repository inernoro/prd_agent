    /// <summary>
    /// 根据模板 key 取对应的模板正文（拼到 system prompt 最后）。
    /// </summary>
    public static string GetTemplate(string templateKey) => templateKey switch
    {
        TemplateKeys.EngineeringMain => EngineeringMainTemplate,
        TemplateKeys.EngineeringSub => EngineeringSubTemplate,
        TemplateKeys.Agile => AgileTemplate,
        _ => EngineeringMainTemplate,
    };

    /// <summary>
    /// 拼装完整 system prompt：核心规则 + 选定模板 + 阶段提示。
    /// </summary>
    public static string BuildSystemPrompt(string templateKey, string phase)
    {
        var template = GetTemplate(templateKey);
        var phaseHint = phase switch
        {
            "B" => @"

## 当前阶段
**阶段二（Part B）**：用户已确认 Part A 方向。请直接输出 Part B 章节内容，不要再重复 Part A，也不要再让用户确认。

",
            _ => @"

## 当前阶段
**阶段一（Part A）**：先输出 Part A 完整内容，结尾必须以提示语收尾让用户确认方向，**禁止本轮直接输出 Part B**。

",
        };
        return SkillCore + "\n\n" + template + phaseHint;
    }
