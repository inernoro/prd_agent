### 阶段一：Part A 价值层（先写）
价值对齐与立项决策 — 确保方向和思路正确。
覆盖：文档头、项目目的、现状背景、用户范围、实现思路、版本迭代规划、需求变更。

### 阶段二：Part B 设计层 / 全局设计与规范层（后写）
为 AI 开发提供足够上下文 — 定义全局设计与规范。
